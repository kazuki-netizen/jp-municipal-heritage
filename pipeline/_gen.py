def cat(label):
    label = label.strip()
    if label in CATMAP:
        return CATMAP[label]
    for k, v in CATMAP.items():
        if k in label:
            return v
    return (None, label)

def wareki_to_iso(s):
    if not s:
        return None
    s = s.strip()
    m = re.match(r'(\d{4})年\s*(\d{1,2})月\s*(\d{1,2})日', s)
    if m:
        y, mo, d = m.groups()
        return f"{int(y):04d}-{int(mo):02d}-{int(d):02d}"
    m = re.match(r'(\d{4})年\s*(\d{1,2})月', s)
    if m:
        y, mo = m.groups()
        return f"{int(y):04d}-{int(mo):02d}"
    m = re.match(r'(\d{4})年', s)
    if m:
        return f"{m.group(1)}"
    return None
