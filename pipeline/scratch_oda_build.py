def wareki_oda(s):
    s = s.strip()
    m = re.match(r'(明|大|昭|平|令)\s*(元|\d+)\.\s*(\d+)\.\s*(\d+)', s)
    if not m:
        return None
    era_c, y, mo, d = m.groups()
    era_map = {'明':'明治','大':'大正','昭':'昭和','平':'平成','令':'令和'}
    era = era_map[era_c]
    y = 1 if y == '元' else int(y)
    mo = int(mo); d = int(d)
    year = ERA_START[era] + y - 1
    return f'{year:04d}-{mo:02d}-{d:02d}'

