import json, re

def wareki_to_iso(s):
    s = s.strip()
    m = re.match(r'^(昭和|平成|令和)(\d+)年(\d+)月(\d+)日$', s)
    if not m:
        return None
    era, y, mo, d = m.groups()
    y=int(y); mo=int(mo); d=int(d)
    base = {"昭和":1925, "平成":1988, "令和":2018}[era]
    return f"{base+y:04d}-{mo:02d}-{d:02d}"

