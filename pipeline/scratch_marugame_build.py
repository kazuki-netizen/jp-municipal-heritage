def wshort(s):
    if not s:
        return None
    s = s.strip().translate(str.maketrans('０１２３４５６７８９．', '0123456789.'))
    m = re.match(r'(明治|大正|昭和|平成|令和|明|大|昭|平|令)\s*(\d+)\s*\.\s*(\d+)\s*\.\s*(\d+)', s)
    if not m:
        return None
    era_c, y, mo, d = m.groups()
    era_map = {'明':'明治','大':'大正','昭':'昭和','平':'平成','令':'令和',
               '明治':'明治','大正':'大正','昭和':'昭和','平成':'平成','令和':'令和'}
    era = era_map[era_c]
    y=int(y); mo=int(mo); d=int(d)
    year = ERA_START[era] + y - 1
    return f'{year:04d}-{mo:02d}-{d:02d}'

