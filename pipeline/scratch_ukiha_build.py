import json

def wareki(s):
    era_map = {"S":1925,"H":1988,"R":2018,"T":1911}
    era = s[0]
    base = era_map[era]
    rest = s[1:]
    year_str, rest2 = rest.split(".",1)
    month, day = rest2.split(".")
    year = int(year_str)
    return f"{base+year:04d}-{int(month):02d}-{int(day):02d}"

