ERA_START = {'明治':1868,'大正':1912,'昭和':1926,'平成':1989,'令和':2019}
ERA_MAP = {'明':'明治','大':'大正','昭':'昭和','平':'平成','令':'令和'}

def wdot(s):
    if not s:
        return None
    m = re.match(r'([明大昭平令])(\d+|元)・\s*(\d+)・\s*(\d+)', s.strip())
    if not m:
        return None
    ec, y, mo, d = m.groups()
    era = ERA_MAP[ec]
    y = 1 if y == '元' else int(y)
    year = ERA_START[era] + y - 1
    return f'{year:04d}-{int(mo):02d}-{int(d):02d}'

