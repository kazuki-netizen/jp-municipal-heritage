import json, re

def wareki_to_iso(s):
    s = s.strip()
    m = re.match(r'^([SHR])(\d+)\.(\d+)\.(\d+)$', s)
    if not m:
        return None
    era, y, mo, d = m.groups()
    y = int(y); mo=int(mo); d=int(d)
    if era == 'S':
        year = 1925 + y
    elif era == 'H':
        year = 1988 + y
    elif era == 'R':
        year = 2018 + y
    return f"{year:04d}-{mo:02d}-{d:02d}"

