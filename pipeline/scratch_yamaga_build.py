def main():
    m = json.load(open(os.path.join(HERE, "cache", "熊本県", "yamaga", "manifest.json"), encoding="utf-8"))
    entry = [v for v in m.values() if v["url"] == SRC_URL][0]
    fetched_at = entry["fetched_at"]

    out_rows = []
    groups = [
        (BUILDINGS, "有形文化財", "建造物"),
        (PAINTING, "有形文化財", "絵画"),
        (SCULPTURE, "有形文化財", "彫刻"),
        (CRAFT, "有形文化財", "工芸"),
        (DOCUMENT, "有形文化財", "古文書"),
        (ARCHAEOLOGY, "有形文化財", "考古資料"),
        (INTANGIBLE, "無形文化財", "無形文化財"),
        (FOLK_TANGIBLE, "民俗文化財", "有形民俗文化財"),
        (FOLK_INTANGIBLE, "民俗文化財", "無形民俗文化財"),
        (SHISEKI, "記念物(史跡・名勝・天然記念物)", "史跡"),
        (MEISHO, "記念物(史跡・名勝・天然記念物)", "名勝"),
        (TENNEN, "記念物(史跡・名勝・天然記念物)", "天然記念物"),
    ]
    for rows, category, subcat in groups:
        for num, name, loc, owner, date in rows:
            r = make_row(num, subcat, name, loc, owner, date, category, subcat)
            r["fetched_at"] = fetched_at
            out_rows.append(r)

    assert len(out_rows) == 160, len(out_rows)
    with open(OUT, "w", encoding="utf-8") as f:
        for r in out_rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"wrote {len(out_rows)} rows to {OUT}")


if __name__ == "__main__":
    main()
