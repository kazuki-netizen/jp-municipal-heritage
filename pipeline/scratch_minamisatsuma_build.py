def parse_date(s):
    s = s.replace(' ', '')
    m = re.match(r'([MTSHR])(\d+)\.(\d+)\.(\d+)', s)
    if not m:
        return None
    c, y, mo, d = m.groups()
    return wareki(NENGO_CHAR[c], y, mo, d)

