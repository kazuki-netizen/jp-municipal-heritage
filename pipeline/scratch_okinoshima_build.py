def wareki_code(s):
    s = s.strip()
    m = re.match(r'(H|S|R|M|T)0*(\d+)\.(\d+)\.(\d+)', s)
    if not m:
        return None
    era_c, y, mo, d = m.groups()
    era_map = {'M':'明治','T':'大正','S':'昭和','H':'平成','R':'令和'}
    era = era_map[era_c]
    y = int(y); mo = int(mo); d = int(d)
    year = ERA_START[era] + y - 1
    return f'{year:04d}-{mo:02d}-{d:02d}'

