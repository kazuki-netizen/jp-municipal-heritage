NENGO_RE = r'(明治|大正|昭和|平成|令和)'

def parse_date(s):
    m = re.search(NENGO_RE + r'(\d+|元)年(\d+)月(\d+)日', s)
    if not m:
        return None
    return wareki(*m.groups())

# heuristic name-based classification (best-effort; not independently
# confirmed per item since the source page only offers a name+date list,
# not a category column — flagged as inference in coverage notes)
PATTERNS = [
    (r'田の?神|庚申像|庚申塔|石敢当|板碑|供養塔|土壙群|笠塔婆|無縫塔|群像|随神像(?!.*群)', '民俗文化財', '有形民俗文化財'),
    (r'神舞|踊り|製作技術', '民俗文化財', '無形民俗文化財'),
    (r'イチイガシ|おがたま|バクチノキ|広葉杉|群生地|フクロハイゴケ', '記念物', '天然記念物'),
    (r'城跡|遺跡|古墳|洞穴|一里塚|陣地跡|寺院跡|仮屋跡|通信壕跡|製鉄所跡|の墓$|夫妻の墓|水際|寺跡$', '記念物', '史跡'),
]

records = []
for line in txt.strip().split('\n'):
    m = re.match(r'^(.+?)[（(](.+)[）)]$', line)
    name, date_s = m.groups()
    date = parse_date(date_s)
    cat = sub = None
    for pat, c, s in PATTERNS:
        if re.search(pat, name):
            cat, sub = c, s
            break
    if cat is None:
        if '墓' in name:
            cat, sub = '記念物', '史跡'
        else:
            cat, sub = '有形文化財', None
