results = []
for group, (cat, subcat) in GROUPS:
    last_date = None
    for name, loc, owner, date_raw, desc in group:
        if date_raw is not None:
            last_date = date_raw
        date = wareki_to_iso(last_date) if last_date else None
        full_desc = desc
        if owner:
            full_desc = f'所有（管理）者: {owner}、{desc}' if desc else f'所有（管理）者: {owner}'
        results.append({
            "pref": "大分県", "municipality": "大分市", "name": name,
            "category": cat, "subcategory": subcat, "designation": "市指定",
            "designated_date": date, "location": loc or None, "description": full_desc or None,
            "source_url": SRC, "source_format": "pdf", "fetched_at": FETCHED,
        })

with open('out/大分県/oita.jsonl', 'w') as f:
    for r in results:
        f.write(json.dumps(r, ensure_ascii=False) + '\n')

