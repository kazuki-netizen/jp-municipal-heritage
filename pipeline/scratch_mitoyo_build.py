import json, re

def wshort2(s):
    # format like S60年05月24日 / H01年02月28日 / R03年08月19日 / M37年08月29日 / T02年08月20日
    era_map = {'M':'明治','T':'大正','S':'昭和','H':'平成','R':'令和'}
    ERA_START = {'明治':1868,'大正':1912,'昭和':1926,'平成':1989,'令和':2019}
    m = re.match(r'([MTSHR])(\d+)年(\d+)月(\d+)日', s)
    if not m:
        return None
    ec, y, mo, d = m.groups()
    era = era_map[ec]
    year = ERA_START[era] + int(y) - 1
    return f'{year:04d}-{int(mo):02d}-{int(d):02d}'

