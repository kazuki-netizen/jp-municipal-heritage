ERA = {"明治": 1867, "大正": 1911, "昭和": 1925, "平成": 1988, "令和": 2018}


def wareki_to_iso(s):
    m = re.match(r"^(明治|大正|昭和|平成|令和)(\d+)年\s*(\d+)月\s*(\d+)日$", s)
    era, y, mo, d = m.groups()
    year = ERA[era] + int(y)
    date = f"{year:04d}-{int(mo):02d}-{int(d):02d}"
    if date > "2026-07-10":
        return None
    return date


