def main():
    m = json.load(open(os.path.join(HERE, "cache", "熊本県", "minamata", "manifest.json"), encoding="utf-8"))
    entry = [v for v in m.values() if v["url"] == SRC_URL][0]
    fetched_at = entry["fetched_at"]

    out_rows = []
    for num, kubun, name, date, loc in ROWS:
        category, subcat = CAT[kubun]
        out_rows.append({
            "pref": "熊本県", "municipality": "水俣市", "name": name,
            "category": category, "subcategory": subcat, "designation": "市指定",
            "designated_date": wareki_to_iso(date), "location": loc,
            "description": f"指定番号{num}",
            "source_url": SRC_URL, "source_format": "pdf", "fetched_at": fetched_at,
        })

    assert len(out_rows) == 30, len(out_rows)
    with open(OUT, "w", encoding="utf-8") as f:
        for r in out_rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"wrote {len(out_rows)} rows to {OUT}")


if __name__ == "__main__":
    main()
