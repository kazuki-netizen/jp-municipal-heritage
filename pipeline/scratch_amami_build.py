NENGO_RE = r'(明治|大正|昭和|平成|令和)'

def parse_date_kanji(s):
    m = re.search(NENGO_RE + r'(\d+|元)年(\d+)月(\d+)日', s)
    if m:
        return wareki(*m.groups())
    m = re.match(r'([MTSHR])(\d+)年(\d+)月(\d+)日', s)
    if m:
        c = {'M': '明治', 'T': '大正', 'S': '昭和', 'H': '平成', 'R': '令和'}[m.group(1)]
        return wareki(c, m.group(2), m.group(3), m.group(4))
    return None

SUB_MAP = {
    '史跡': ('記念物', '史跡'), '名勝': ('記念物', '名勝'), '天然記念物': ('記念物', '天然記念物'),
    '有形民俗文化財': ('民俗文化財', '有形民俗文化財'), '無形民俗文化財': ('民俗文化財', '無形民俗文化財'),
}

def classify(kind):
    if kind in SUB_MAP:
        return SUB_MAP[kind]
    m = re.match(r'有形文化財（(.+)）', kind)
    if m:
        return '有形文化財', m.group(1)
    return None, kind

